package com.ruoyi.common.security.utils;

import javax.servlet.http.HttpServletRequest;
import org.springframework.security.crypto.bcrypt.BCryptPasswordEncoder;
import com.ruoyi.common.core.constant.SecurityConstants;
import com.ruoyi.common.core.constant.TokenConstants;
import com.ruoyi.common.core.constant.UserConstants;
import com.ruoyi.common.core.context.SecurityContextHolder;
import com.ruoyi.common.core.exception.ServiceException;
import com.ruoyi.common.core.utils.ServletUtils;
import com.ruoyi.common.core.utils.StringUtils;
import com.ruoyi.system.api.model.LoginUser;

/**
 * 权限获取工具类
 * 
 * @author ruoyi
 */
public class SecurityUtils
{
    /**
     * 获取用户ID
     */
    public static Long getUserId()
    {
        return SecurityContextHolder.getUserId();
    }

    /**
     * 获取用户名称
     */
    public static String getUsername()
    {
        return SecurityContextHolder.getUserName();
    }

    /**
     * 获取用户key
     */
    public static String getUserKey()
    {
        return SecurityContextHolder.getUserKey();
    }

    /**
     * 获取登录用户信息
     */
    public static LoginUser getLoginUser()
    {
        return SecurityContextHolder.get(SecurityConstants.LOGIN_USER, LoginUser.class);
    }

    /**
     * 获取请求token
     */
    public static String getToken()
    {
        return getToken(ServletUtils.getRequest());
    }

    /**
     * 根据request获取请求token
     */
    public static String getToken(HttpServletRequest request)
    {
        // 从header获取token标识
        String token = request.getHeader(SecurityConstants.AUTHORIZATION_HEADER);
        return replaceTokenPrefix(token);
    }

    /**
     * 裁剪token前缀
     */
    public static String replaceTokenPrefix(String token)
    {
        // 如果前端设置了令牌前缀，则裁剪掉前缀
        if (StringUtils.isNotEmpty(token) && token.startsWith(TokenConstants.PREFIX))
        {
            token = token.replaceFirst(TokenConstants.PREFIX, "");
        }
        return token;
    }

    /**
     * 是否为管理员
     * 
     * @param userId 用户ID
     * @return 结果
     */
    public static boolean isAdmin(Long userId)
    {
        return userId != null && 1L == userId;
    }

    /**
     * 生成BCryptPasswordEncoder密码
     *
     * @param password 密码
     * @return 加密字符串
     */
    public static String encryptPassword(String password)
    {
        BCryptPasswordEncoder passwordEncoder = new BCryptPasswordEncoder();
        return passwordEncoder.encode(password);
    }

    /**
     * 判断密码是否相同
     *
     * @param rawPassword 真实密码
     * @param encodedPassword 加密后字符
     * @return 结果
     */
    public static boolean matchesPassword(String rawPassword, String encodedPassword)
    {
        BCryptPasswordEncoder passwordEncoder = new BCryptPasswordEncoder();
        return passwordEncoder.matches(rawPassword, encodedPassword);
    }

    /**
     * 校验管理员权限
     * 
     * @param permission 权限位
     * @throws ServiceException 无权限异常
     */
    public static void checkAdminPermission(int permission)
    {
        LoginUser loginUser = getLoginUser();
        if (loginUser == null || loginUser.getSysUser() == null)
        {
            throw new ServiceException("获取用户信息失败，请重新登录");
        }
        
        // 超级管理员拥有所有权限
        Integer adminLevel = loginUser.getSysUser().getAdminLevel();
        if (adminLevel != null && UserConstants.ADMIN_LEVEL_SUPER == adminLevel)
        {
            return;
        }
        
        // 普通管理员根据权限位判断
        Integer permissions = loginUser.getSysUser().getPermissions();
        if (permissions == null || (permissions & permission) != permission)
        {
            throw new ServiceException("操作失败，您没有该项操作的权限，请联系超级管理员分配");
        }
    }

    /**
     * 是否为超级管理员
     * 
     * @return 结果
     */
    public static boolean isSuperAdmin()
    {
        LoginUser loginUser = getLoginUser();
        if (loginUser == null || loginUser.getSysUser() == null)
        {
            return false;
        }
        Integer adminLevel = loginUser.getSysUser().getAdminLevel();
        return adminLevel != null && UserConstants.ADMIN_LEVEL_SUPER == adminLevel;
    }
}
